class Car:
    def move_forward(self):
        print("Moving forward")
    def move_backward(self):
        print("Moving backward")
    def turn_left(self):
        print("Turning left")
    def turn_right(self):
        print("Turning right")

ferrari = Car()
ferrari.move_forward()
ferrari.move_backward()

mustang = Car()
mustang.move_forward()
mustang.move_backward()
mustang.turn_left()
mustang.turn_right()


